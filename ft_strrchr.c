#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = c;
	while (i >= 1)
	{
		if (s[i - 1] == (char)i)
			return ((char *)s + i - 1);
		i--;
	}
	return (NULL);
}
